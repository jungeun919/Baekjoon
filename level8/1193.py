# 1/1 -> 1개 (n부터)
# 1/2 2/1 -> 2개 (1부터)
# 3/1 2/2 1/3 -> 3개 (n부터)
# 1/4 2/3 3/2 4/1 -> 4개 (1부터)

# a/b
# a = [1] [1, 2] [3, 2, 1] [1, 2, 3, 4]
# b = [1] [2, 1] [1, 2, 3] [4, 3, 2, 1]

X = int(input())

line = 1
while X > line: # X가 5일 경우
    X -= line # 5 4 2 (2번째 열)
    line += 1 # 1 2 3 (3번째 행)

if line % 2 == 0:
    a = X
    b = line - X + 1
else:
    a = line - X + 1
    b = X

print(a, "/", b, sep="")